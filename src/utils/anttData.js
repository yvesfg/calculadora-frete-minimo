export function resolveTable(hp, fc) {
  if (hp && fc)  return 'C';
  if (hp && !fc) return 'D';
  if (!hp && fc) return 'A';
  return 'B';
}

export function findRow(tbl, cargo, axles) {
  return RAW.find(r => r[IDX.TBL] === tbl && r[IDX.CARGO] === cargo && r[IDX.AXLES] === axles) || null;
}

export function calcPiso(row, dist) {
  if (!row || !dist) return null;
  return row[IDX.CCD] * dist + row[IDX.CC];
}

export function fmtBRL(v) {
  if (v == null || isNaN(v)) return '—';
  return v.toLocaleString('pt-BR', { style:'currency', currency:'BRL' });
}

export function fmtNum(v, dec = 2) {
  if (v == null || isNaN(v)) return '—';
  return v.toLocaleString('pt-BR', { minimumFractionDigits: dec, maximumFractionDigits: dec });
}
